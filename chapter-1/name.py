#2 Testing different String methods

name="ada lovelace craft"
print("title method:", name.title())
print("capitalize method:", name.capitalize())
print("casefold method:", name.casefold())
print("center method:", name.center(30))
print("count method: ", name.count('a'))
print("String array: ", name[1])
print("encode method: ", name.encode())
print("endswitch method: ", name.endswith('t'))
print("expandtabs method: ", name.expandtabs())
print("find method: ", name.find('craft'))
print(name.format())